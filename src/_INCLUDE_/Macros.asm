; Convert a string to the textbox's format and store as a line
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; PARAMETERS:
;	str	- The string
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; RETURNS:
;	Nothing
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
txtLine		macro	str
c		= 1
		rept	strlen(\str)
ochar			substr c, c, \str
char			substr "\ochar"-$1F, "\ochar"-$1F, "skojh`aledisfsg\0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\]^_ABCDEFGHIJKLMNOPQRSTUVWXYZncmb"
			dc.w	"\char"+$588
c			= c+1
		endr
		dc.w	$FFFF
		endm
